package com.example.hivemanager;

public class HiveObject {
    private String apiary;
    private String hive;
    private String inspection;
    private String health;
    private String honey;
    private String queenProduction;
    private String equipHive;
    private String equipInven;
    private int loss;
    private int gain;

    public String getApiary() {
        return apiary;
    }

    public void setApiary(String apiary) {
        this.apiary = apiary;
    }

    public String getHive() {
        return hive;
    }

    public void setHive(String hive) {
        this.hive = hive;
    }

    public String getInspection() {
        return inspection;
    }

    public void setInspection(String inspection) {
        this.inspection = inspection;
    }

    public String getHealth() {
        return health;
    }

    public void setHealth(String health) {
        this.health = health;
    }

    public String getHoney() {
        return honey;
    }

    public void setHoney(String honey) {
        this.honey = honey;
    }

    public String getQueenProduction() {
        return queenProduction;
    }

    public void setQueenProduction(String queenProduction) {
        this.queenProduction = queenProduction;
    }

    public String getEquipHive() {
        return equipHive;
    }

    public void setEquipHive(String equipHive) {
        this.equipHive = equipHive;
    }

    public String getEquipInven() {
        return equipInven;
    }

    public void setEquipInven(String equipInven) {
        this.equipInven = equipInven;
    }

    public int getLoss() {
        return loss;
    }

    public void setLoss(int loss) {
        this.loss = loss;
    }

    public int getGain() {
        return gain;
    }

    public void setGain(int gain) {
        this.gain = gain;
    }
}
